function openSignInModal() {
    const signInModal = document.querySelector('.sign-in-modal');
    const signUpModal = document.querySelector('.sign-up-modal');

    signUpModal.close()
    signInModal.showModal()
}

function openSignUpModal(){
    const signInModal = document.querySelector('.sign-in-modal');
    const signUpModal = document.querySelector('.sign-up-modal');
  
    signInModal.close()
    signUpModal.showModal()
  }
  